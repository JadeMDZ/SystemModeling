import numpy as np
# Параметры
N = 1000
Mx = 5 / 6
Dx = 5 / 252
m, g = 0, 0
# Вычисления
for i in range(N):
    r = np.random.uniform(0, 1)
    x = ((6 * r) / 5) ** (1 / 5)
    m += x
    g += x * x

m = m / N
g = g / (N - 1) - m ** 2

Mxn = abs(Mx - m)
Dxq = abs(Dx - g)

print('')
print("┌───────┬──────────────────┬──────────────────┬──────────────────┬──────────────────┬──────────────────┬──────────────────┐")
print("│   N   │        Mx        │        m         │      |Mx-m|      │        Dx        │         g        │      |Dx-g|      │")
print("├───────┼──────────────────┼──────────────────┼──────────────────┼──────────────────┼──────────────────┼──────────────────┤")
print(f"│ {N:^5} │ {Mx:^16.3f} │ {m:^16.3f} │ {Mxn:^16.3f} │ {Dx:^16.3f} │ {g:^16.3f} │ {Dxq:^16.3f} │")
print("└───────┴──────────────────┴──────────────────┴──────────────────┴──────────────────┴──────────────────┴──────────────────┘")